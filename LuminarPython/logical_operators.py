# Logical operators
 # AND, OR, NOT

# AND
# print(10>5 and 10>6)    #True  True  True
# print(5<10 and 10<6)    #True  False  False
# print(10<5 and 6<10)    #False  True  False
# print(10<5 and 6<10)    #False  False  False


# OR
# print(10>5 or 10>6)       #True  True  True
# print(10>5 or 6>10)       #True  False  True
# print(5>10 or 6<10)       #False  True  True
# print(5>10 or 6>10)       #False  False  False


# NOT
print(not 5>6)            #True
print(not 5<6)            #False