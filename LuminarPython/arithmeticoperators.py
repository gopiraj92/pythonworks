# Arithmetic Operators
    # Addition +
    # Subtraction -
    # Multiplication *
    # Division /
    # Modulus %   - gives Reminder as output
    # Floor division //   - gives integer part as output
    # Exponent **   - a**b --> a power b
a = 3
b = 2
sum = a+b
sub = a-b
mul = a*b
div = a/b
mod = a%b
flr = a//b
exp = a**b
print("Sum is:", sum)
print("Difference is:", sub)
print("Multiplication is:", mul)
print("Division is:", div)
print("Reminder is:", mod)
print("Floor division is:", flr)
print("Exponent is:", exp)
